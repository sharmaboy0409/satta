package com.game.dubaisattagame.models;

public class InputNodata {
    public String inputdata;
    public String inpunotdata;

    public InputNodata(String inputdata, String inpunotdata) {
        this.inputdata = inputdata;
        this.inpunotdata = inpunotdata;
    }

    public String getInputdata() {
        return inputdata;
    }

    public void setInputdata(String inputdata) {
        this.inputdata = inputdata;
    }

    public String getInpunotdata() {
        return inpunotdata;
    }

    public void setInpunotdata(String inpunotdata) {
        this.inpunotdata = inpunotdata;
    }
}
